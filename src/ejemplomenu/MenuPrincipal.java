    /**
     * Metodo que define el icono para los botones
     *
     * @param path
     * @param boton
     * @return
     */
    public final Icon definirIcono(String path, JButton boton) {
        ImageIcon icono = new ImageIcon(getClass().getResource(path));//Instanciamos un objeto de tipo ImageIcon
        int ancho = boton.getWidth();//Define el ancho de la imagen en funcion del ancho del boton
        int alto = boton.getHeight();//Define el alto de la imagen en funcion del alto del boton
        ImageIcon iconoDevuelto = new ImageIcon(icono.getImage().getScaledInstance(ancho, alto, Image.SCALE_DEFAULT));
        return iconoDevuelto;
    }

    /**
     * Metodo que crea el efecto al dar clic al boton
     *
     * @param path
     * @param boton
     * @param ancho
     * @param alto
     * @return
     */
    public final Icon efectoBoton(String path, JButton boton, int ancho, int alto) {
        ImageIcon icono = new ImageIcon(getClass().getResource(path));
        int width = boton.getWidth() - ancho;
        int heigth = boton.getHeight() - alto;
        ImageIcon iconoDevuelto = new ImageIcon(icono.getImage().getScaledInstance(width, heigth, Image.SCALE_DEFAULT));
        return iconoDevuelto;
    }

