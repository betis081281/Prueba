    private void btnMatrizclicUsuarios(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatrizclicUsuarios
        String nombres[][] = new String[2][3];
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 3; j++) {
                nombres[i][j] = JOptionPane.showInputDialog("Ingrese un equipo");
            }
        }
        String cadena = "";
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 3; j++) {
                cadena += nombres[i][j] + " ";
            }
            cadena += "\n";
        }
        JOptionPane.showMessageDialog(null, cadena);
    }//GEN-LAST:event_btnMatrizclicUsuarios

